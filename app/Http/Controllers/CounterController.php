     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $home_counter = new Counter();
        $home_counter->under_treatment = $request->under_treatment;
        $home_counter->treated_case = $request->treated_case;
        $home_counter->number_session = $request->number_session;
        $home_counter->number_patient = $request->number_patient;
        
        $home_counter->save();
        toastr()->success('Data Sucessfully Added');
        return redirect()->back();
    }

